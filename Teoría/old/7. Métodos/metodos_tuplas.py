# Métodos para tuplas
["count", "index"]

# Crear una tupla
tupla = (1, 2, 3)

# Contar el número de veces que aparece un elemento en la tupla
conteo = tupla.count(2)  # Contar cuántas veces aparece el elemento 2

# Encontrar el índice de la primera aparición de un elemento en la tupla
indice = tupla.index(3)  # Índice de la primera aparición del elemento 3