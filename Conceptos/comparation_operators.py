# Comparisons return a boolean value: True or False.

# Equality and Inequality
a = 5
b = 6

is_equal = a == b  # False -> a is not equal to b
is_not_equal = a != b  # True -> a is not equal to b

# Less Than & Greater Than
less_than = a < b  # True -> a is less than b
greater_than = a > b  # False -> a is not greater than b

# Less or Equal & Greater or Equal
less_or_equal = a <= b  # True -> a is less than or equal to b
greater_or_equal = a >= b  # False -> a is not greater than or equal to b

