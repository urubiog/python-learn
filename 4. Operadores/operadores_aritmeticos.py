# Suma y resta
suma = 12 + 5
print(suma)
resta = 12 - 5
print(resta)

# Multiplicación y división
multi = 12 * 5
print(multi)
division = 12 / 5 # Devuelve dato float independientemente de los números
print(division)

# Potencia
exponente = 12 ** 5
print(exponente)

# División baja
division_entera = 12 // 5 # Solo se obtiene la parte entera
print(division_entera)

# Módulo
resto = 12 % 5 # Devuelve el resto de la división
print(resto)